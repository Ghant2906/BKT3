package com.example.bkt3;

public class Thi {
    String tenThi, moTa;

    public Thi() {
    }

    public Thi(String tenThi, String moTa) {
        this.tenThi = tenThi;
        this.moTa = moTa;
    }

    public String getTenThi() {
        return tenThi;
    }

    public void setTenThi(String tenThi) {
        this.tenThi = tenThi;
    }

    public String getMoTa() {
        return moTa;
    }

    public void setMoTa(String moTa) {
        this.moTa = moTa;
    }
}
